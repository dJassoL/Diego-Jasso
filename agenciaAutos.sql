-- CREAMOS LA BASE --
DROP DATABASE IF EXISTS rapidos_furiosos;

CREATE DATABASE IF NOT EXISTS rapidos_furiosos;

-- HABILITAMOS LA BASE CREADA --

USE rapidos_furiosos;

-- CREAMOS LAS TABLAS PADRES -- 

CREATE TABLE estado(
id_estado SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
estado VARCHAR(100) NOT NULL);

CREATE TABLE marca(
id_marca SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
marca VARCHAR(50) NOT NULL);

CREATE TABLE modelo(
id_modelo SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
modelo VARCHAR(50) NOT NULL);

-- CREAMOS LAS TABLAS HIJAS O DEPENDIENTES --

CREATE TABLE municipio(
id_municipio SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_estado SMALLINT UNSIGNED NOT NULL,
municipio VARCHAR(100) NOT NULL,
CONSTRAINT fk_mun_edo 
FOREIGN KEY(id_estado) REFERENCES estado(id_estado)
ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE direccion(
id_direccion SMALLINT UNSIGNED NOT NULL	AUTO_INCREMENT PRIMARY KEY,
id_municipio SMALLINT UNSIGNED NOT NULL,
calle VARCHAR(150) NOT NULL,
colonia VARCHAR(100) NOT NULL,
cp CHAR(5) NOT NULL,
numero_ext VARCHAR(10) NOT NULL,
numero_int VARCHAR(10),
CONSTRAINT fk_dir_mun
FOREIGN KEY(id_municipio) REFERENCES municipio(id_municipio)
ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE cliente(
id_cliente SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_direccion SMALLINT UNSIGNED NOT NULL,
nombre VARCHAR(30) NOT NULL,
apellido_pa VARCHAR(30) NOT NULL,
apellido_ma VARCHAR(30),
fecha_nacimiento DATE NOT NULL,
UNIQUE KEY(id_direccion),
CONSTRAINT fk_cli_dir
FOREIGN KEY(id_direccion) REFERENCES direccion(id_direccion)
ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE sucursal(
id_sucursal SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT,
nombre_sucursal VARCHAR(100) NOT NULL UNIQUE KEY,
PRIMARY KEY(id_sucursal),
CONSTRAINT fk_suc_dir
FOREIGN KEY(id_sucursal) REFERENCES direccion(id_direccion));

CREATE TABLE auto(
id_auto SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_marca SMALLINT UNSIGNED NOT NULL,
id_modelo SMALLINT UNSIGNED NOT NULL,
color VARCHAR(20) NOT NULL,
precio DECIMAL(10,2) NOT NULL DEFAULT 0.00,
UNIQUE KEY(id_marca,id_modelo),
CONSTRAINT fk_au_mar
FOREIGN KEY(id_marca) REFERENCES marca(id_marca)
ON DELETE RESTRICT ON UPDATE CASCADE,
CONSTRAINT fk_au_mod
FOREIGN KEY(id_modelo) REFERENCES modelo(id_modelo)
ON DELETE RESTRICT ON UPDATE CASCADE);

CREATE TABLE empleado(
id_empleado SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_sucursal SMALLINT UNSIGNED NOT NULL,
fecha_ingreso DATE NOT NULL,
CONSTRAINT fk_em_suc
FOREIGN KEY(id_sucursal) REFERENCES sucursal(id_sucursal)
ON DELETE RESTRICT ON UPDATE CASCADE);

CREATE TABLE premio(
id_premio SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_empleado SMALLINT UNSIGNED NOT NULL,
fecha DATE NOT NULL,
monto DECIMAL(10,2) NOT NULL DEFAULT 1000.00,
UNIQUE KEY(id_empleado),
CONSTRAINT fk_pr_em
FOREIGN KEY(id_empleado) REFERENCES empleado(id_empleado)
ON DELETE CASCADE ON UPDATE CASCADE);

CREATE TABLE ventas(
id_venta SMALLINT UNSIGNED NOT NULL AUTO_INCREMENT PRIMARY KEY,
id_cliente SMALLINT UNSIGNED NOT NULL,
id_empleado SMALLINT UNSIGNED NOT NULL,
fecha DATE NOT NULL,
UNIQUE KEY(id_cliente,id_empleado),
CONSTRAINT fk_ven_cli
FOREIGN KEY(id_cliente) REFERENCES cliente(id_cliente)
ON DELETE RESTRICT ON UPDATE CASCADE,
CONSTRAINT fk_vent_em
FOREIGN KEY(id_empleado) REFERENCES empleado(id_empleado)
ON DELETE RESTRICT ON UPDATE CASCADE,
CONSTRAINT fk_ven_au
FOREIGN KEY(id_venta) REFERENCES auto(id_auto)
ON DELETE RESTRICT ON UPDATE CASCADE);

